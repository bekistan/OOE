    public function update(Request $request, $id)
    {
        $user =User::find($id);
        $user->name=$request->input('name');
        $user->email=$request->input('email');
        $user->proffesion=$request->input('profession');
        $user->phone_number=$request->input('phone');
        $user->address=$request->input('address');
        $user->educational_status=$request->input('edu_status');
        $user->save();

        return redirect('http://localhost/OOE/public/profileupdate')->with('success','successfully Updated');
